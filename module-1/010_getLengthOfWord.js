/*
Write a function called "getLengthOfWord".
Given a word, "getLengthOfWord" returns the length of the given word.
var output = getLengthOfWord('some');
console.log(output); // --> 4
*/

function getLengthOfWord(word) {
  return word.length;
}

var output = getLengthOfWord("some");
console.log(output); // --> 4
